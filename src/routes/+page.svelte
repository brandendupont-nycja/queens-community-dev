<TextBlock>
John Jay College’s Research & Evaluation Center finds youth under 18 comprise a small and stable slice of violent arrests. Felony-assault arrests climbed from 15,117 (2018) to 22,606 (2024), yet the youth share slipped from 6% to 5%. Adults 25 and older expanded from 74% to 80%. Two decades earlier, youth made up 15% of assault arrests; today they account for one-third of that.
</TextBlock>

<TextBlock>
The NYPD notes one exception: gun violence. From 2018 to 2024, gun arrests of people under 18 rose 136 %, identified shooters +92%, and youth shooting victims +81% (NYC Council & Tisch 2025). Adult comparators aren’t published, but the gap suggests deeper youth exposure to firearms even as overall proportions declined.
</TextBlock>

