This report extends public John Jay REC data with NYPD open files and New York City Criminal Justice Agency (CJA) records from 2018 onward. We track the youth share of felony arrests,total counts, and 365-day felony recidivism,across all felonies, Violent Felony Offenses (VFOs), and a narrow “Serious VFO” subset—assault 1, murder, and attempted murder.We treat 2018 as the baseline and 2021 as the pandemic trough, focusing on ages 13–15 and 16–17 alongside adjacent 18–19-year-olds.

<b>The goal</b>: to see who is driving recent changes in felony arrests and re-offending—and where youth fit in the broader story.  
